#Algoritmo média: criar um algoritmo que leia 4 notas e apresente a média ao final

nota1 = float(input("Digite a sua nota da primeira unidade: "))
nota2 = float(input("Digite a sua nota da segunda unidade: "))
nota3 = float(input("Digite a sua nota da terceira unidade: "))
nota4 = float(input("Digite a sua nota da quarta unidade: "))
média = (nota1 + nota2 + nota3 + nota4) / 4
print("A média final obtida: ", média)
